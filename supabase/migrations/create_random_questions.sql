
-- Create the random_questions table
CREATE TABLE IF NOT EXISTS public.random_questions (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    question_text TEXT NOT NULL,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Enable RLS (Row Level Security)
ALTER TABLE public.random_questions ENABLE ROW LEVEL SECURITY;

-- Drop existing policies to avoid errors on re-run
DROP POLICY IF EXISTS "Allow public read access" ON public.random_questions;
DROP POLICY IF EXISTS "Allow service role full access" ON public.random_questions;

-- Create policy to allow public read access (anyone can read questions)
CREATE POLICY "Allow public read access" ON public.random_questions
    FOR SELECT USING (true);

-- Create policy to allow service role full access (for admin/seeding)
CREATE POLICY "Allow service role full access" ON public.random_questions
    FOR ALL USING (auth.role() = 'service_role');

-- Create a function to get a random question
CREATE OR REPLACE FUNCTION get_random_question()
RETURNS SETOF random_questions
LANGUAGE sql
AS $$
  SELECT *
  FROM random_questions
  ORDER BY random()
  LIMIT 1;
$$;

